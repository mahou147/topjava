package ru.javawebinar.topjava.util;

import ru.javawebinar.topjava.model.UserMeal;
import ru.javawebinar.topjava.model.UserMealWithExcess;

import java.time.LocalDateTime;
import java.time.LocalTime;
import java.time.Month;
import java.util.ArrayList;
import java.util.Arrays;
import java.util.List;

public class UserMealsUtil {
    public static void main(String[] args) {
        List<UserMeal> meals = Arrays.asList(
                new UserMeal(LocalDateTime.of(2020, Month.JANUARY, 30, 10, 0), "Завтрак", 500),
                new UserMeal(LocalDateTime.of(2020, Month.JANUARY, 30, 13, 0), "Обед", 1000),
                new UserMeal(LocalDateTime.of(2020, Month.JANUARY, 30, 20, 0), "Ужин", 500),
                new UserMeal(LocalDateTime.of(2020, Month.JANUARY, 31, 0, 0), "Еда на граничное значение", 100),
                new UserMeal(LocalDateTime.of(2020, Month.JANUARY, 31, 10, 0), "Завтрак", 1000),
                new UserMeal(LocalDateTime.of(2020, Month.JANUARY, 31, 13, 0), "Обед", 500),
                new UserMeal(LocalDateTime.of(2020, Month.JANUARY, 31, 20, 0), "Ужин", 410)
        );
    }

    public static List<UserMealWithExcess> filteredByCycles(List<UserMeal> meals, LocalTime startTime, LocalTime endTime, int caloriesPerDay) {
        TimeUtil timeUtil = new TimeUtil();
        List<UserMealWithExcess> list = new ArrayList<>();
        for (UserMeal m : meals) {
            if (timeUtil.isBetweenHalfOpen(m.getDateTime().toLocalTime(), startTime, endTime))
                list.add(new UserMealWithExcess(m.getDateTime(), m.getDescription(), caloriesPerDay, true));
        }
        return list;
    }

    public static List<UserMealWithExcess> filteredByStreams(List<UserMeal> meals, LocalTime startTime, LocalTime endTime, int caloriesPerDay) {
        TimeUtil timeUtil = new TimeUtil();
        List<UserMealWithExcess> list = new ArrayList<>();
        meals.stream()
                .filter(time -> timeUtil.isBetweenHalfOpen(time.getDateTime().toLocalTime(), startTime, endTime))
                .forEach(m -> list.add(new UserMealWithExcess(m.getDateTime(), m.getDescription(), caloriesPerDay, true)));
        return list;
    }
}
